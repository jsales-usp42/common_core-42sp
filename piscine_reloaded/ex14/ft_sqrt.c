int	ft_sqrt(int nb)
{
	int	sqrt;
	int	i;

	i = 0;
	while (i <= nb && sqrt != nb)
	{
		sqrt = i * i;
		if (sqrt == nb)
			return (i);
		i++;
	}
	return (0);
}
