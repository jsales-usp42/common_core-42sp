#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	size_t	size_s;
	char	*p;

	size_s = ft_strlen(s);
	p = (char *)malloc((size_s + 1) * sizeof(char));
	if (p == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
